let numero = Number(prompt('Digite um número entre 1 e 7:'))

switch(numero) {
    case 1:
        alert('segunda-feira')
        break
    case 2:
        alert('terça-feira')
        break
    case 3:
        alert('quarta-feira')
        break
    case 4:
        alert('quinta-feira')
        break
    case 5:
        alert('sexta-feira')
        break
    case 6:
        alert('sabado')
        break
    case 7:
        alert('domingo')
        break
}